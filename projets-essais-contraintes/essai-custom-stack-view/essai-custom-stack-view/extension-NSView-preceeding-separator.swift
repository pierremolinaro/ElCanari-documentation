import Cocoa

//——————————————————————————————————————————————————————————————————————————————————————————————————————————————————————

@objc enum StackVerticalAlignment : Int {
  case spawn, top, center, baseline, bottom
}

//——————————————————————————————————————————————————————————————————————————————————————————————————————————————————————

@objc enum StackHorizontalAlignment : Int {
  case spawn, left, center, right
}

//——————————————————————————————————————————————————————————————————————————————————————————————————————————————————————

extension NSView {

  //····················································································································

  @objc var hasPreceedingSeparator : Bool { return false }


  //····················································································································

  @objc var verticalAlignment : StackVerticalAlignment { return .center }

  @objc var horizontalAlignment : StackHorizontalAlignment { return .center }

  //····················································································································

}

