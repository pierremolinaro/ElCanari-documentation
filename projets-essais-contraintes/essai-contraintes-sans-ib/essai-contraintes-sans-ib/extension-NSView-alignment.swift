import Cocoa

//----------------------------------------------------------------------------------------------------------------------
//   Vertical alignment: used by EBHorizontalStackView
//----------------------------------------------------------------------------------------------------------------------

@objc enum VerticalAlignment : Int {

  //····················································································································

  case top
  case center
  case height
  case firstBaseline
  case lastBaseline
  case bottom

  //····················································································································

}

//----------------------------------------------------------------------------------------------------------------------

extension NSView {

  //····················································································································

  @objc func verticalAlignment () -> VerticalAlignment { return .center }

  //····················································································································

}

